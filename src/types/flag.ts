// flag.ts - 标志位数据结构

// ============================================================
// 标志位定义
// ============================================================

/**
 * 标志位配置
 * 标志位用于追踪游戏中的各种状态：
 * - 事件是否已触发
 * - 场景描述是否已看过
 * - 物品是否已获得过
 * - 任务进度
 * - 各种解锁状态
 * 等等
 */
export interface Flag {
  /** 标志位唯一ID */
  id: string
  /** 标志位名称（开发者可见，便于调试） */
  name: string
  /** 标志位备注 */
  notes?: string

  /** 标志位数据类型 */
  type: FlagType

  /** 默认值 */
  defaultValue: FlagValue
}

// ============================================================
// 标志位类型
// ============================================================

/**
 * 标志位数据类型
 */
export enum FlagType {
  /** 布尔型（最常用，如"是否触发过某事件"） */
  BOOLEAN = 'boolean',
  /** 数值型（如"击杀某敌人次数"、"捐赠物资数量"） */
  NUMBER = 'number',
  /** 字符串型（如"当前任务阶段名称"、"NPC好感度等级"） */
  STRING = 'string',
}

// ============================================================
// 标志位值
// ============================================================

/**
 * 标志位值类型
 */
export type FlagValue = boolean | number | string

/**
 * 标志位操作
 */
export enum FlagOperation {
  /** 设置值 */
  SET = 'set',
  /** 布尔取反 */
  TOGGLE = 'toggle',
  /** 数值增加 */
  ADD = 'add',
  /** 数值减少 */
  SUBTRACT = 'subtract',
}

// ============================================================
// 标志位注册表
// ============================================================

/**
 * 标志位注册表（全局标志位配置汇总）
 */
export interface FlagRegistry {
  /** 所有标志位配置，按ID索引 */
  flags: Record<string, Flag>
}
